async function main() {
  loadEnv();
  const { createClient } = require('@supabase/supabase-js');
  const supa = createClient(process.env.NEXT_PUBLIC_SUPABASE_URL, process.env.SUPABASE_SERVICE_ROLE_KEY);

  // Read pairings.json
  const pairingsPath = path.join(__dirname, 'pairings.json');
  if (!fs.existsSync(pairingsPath)) {
    throw new Error('pairings.json not found. Run get-pairings.js first.');
  }
  const pairings = JSON.parse(fs.readFileSync(pairingsPath, 'utf8'));

  const { data: gen } = await supa.from('generators')
    .select('id, slug, version')
    .eq('slug', 'response-gen')
    .eq('status', 'active')
    .order('version', { ascending: false })
    .limit(1)
    .single();

  const { data: run, error: rErr } = await supa.from('generation_runs').insert({
    generator_id: gen ? gen.id : null,
    generator_slug: 'response-gen',
    generator_ver: gen ? gen.version : 1,
    params: { source: 'antigravity-expert-responses', seed: SEED },
    model_used: 'gemini-3.5-flash/antigravity-v2',
    items_requested: pairings.length * 3,
    status: 'running',
  }).select().single();
  if (rErr) throw rErr;

  let created = 0;
  for (const p of pairings) {
    // Determine profile key for responses lookup
    const key = `${p.mbti}_${p.decision_style}`;
    const customResponses = RESPONSES_DATA[key];
    if (!customResponses) {
      console.warn(`No custom responses defined for profile key: ${key}`);
      continue;
    }

    for (const sc of p.scenarios) {
      // Find matching response in our custom data
      const respInfo = customResponses.find(cr => sc.title.includes(cr.scenario_title_match));
      if (!respInfo) {
        console.warn(`No response match for scenario: ${sc.title} under profile key: ${key}`);
        continue;
      }

      // Insert response
      const { data: row, error: iErr } = await supa.from('profile_scenario_responses').insert({
        profile_id: p.profile_id,
        scenario_id: sc.id,
        response: respInfo.response,
        reasoning_chain: respInfo.reasoning_chain,
        emotional_arc: respInfo.emotional_arc,
        confidence: respInfo.confidence,
        generation_run_id: run.id,
      }).select('id').single();

      if (iErr) {
        console.error(`Response insert failed for profile ${p.profile_id} scenario ${sc.id}:`, iErr.message);
        continue;
      }

      created++;

      // Insert provenance
      await supa.from('provenance').insert({
        entity_type: 'profile_scenario_response',
        entity_id: row.id,
        entity_version: 1,
        model: 'gemini-3.5-flash/antigravity-v2',
        prompt_hash: sha256(`response profile=${p.profile_id} scenario=${sc.id} seed=${SEED}`),
        template_hash: sha256('antigravity-response-v2'),
        params: { source: 'antigravity-expert-responses', pattern: respInfo.emotional_arc, seed: SEED },
        sha256_content: sha256(canonical({
          response: respInfo.response,
          reasoning_chain: respInfo.reasoning_chain,
          emotional_arc: respInfo.emotional_arc,
          confidence: respInfo.confidence
        })),
      });
    }
  }

  await supa.from('generation_runs').update({
    items_created: created,
    status: 'done',
    finished_at: new Date().toISOString()
  }).eq('id', run.id);

  console.log(`Successfully inserted ${created} high-quality responses. Run ${run.id}.`);
}

main().catch(console.error);
